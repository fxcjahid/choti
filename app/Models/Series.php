<?php

namespace App\Models;

use App\Models\Post;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Support\Str;

class Series extends Model
{
    use HasFactory;

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'user_id',
        'name',
        'slug',
        'description',
        'is_active',
    ];

    protected $casts = [
        'is_active' => 'boolean',
    ];

    /**
     * The accessors to append to the model's array form.
     *
     * @var array
     */

    /** protected $appends = ['posts']; */

    protected static function boot()
    {
        parent::boot(); // TODO: Change the autogenerated stub

        static::creating(function ($series) {
            // Set auth user id
            $series->user_id = auth()->id();

            // Create Category URL
            if (empty($series->slug)) {
                $series->slug = Str::slug($series->name);
            }

            // Set Category Status
            if (empty($series->is_active)) {
                $series->is_active = true;
            }
        });
    }

    /**
     * Get publish category link for Sitemap
     */
    public static function sitemap()
    {
        return self::where('is_active', '=', true)
            ->get()
            ->map(function ($each) {
                return [
                    'url' => route('series', ['series' => $each->slug]),
                ];
            });
    }

    /**
     * Get post attached
     * Relation between table
     */
    public function post()
    {
        return $this->belongsToMany(Post::class, 'post_series');
    }
}